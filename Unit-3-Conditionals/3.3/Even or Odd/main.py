num = int(input("Enter a number: "))
if num % 2 == 0:
    print ("Even")
#if the user enters a number that is divisible by 2, then "Even" will be printed
else:
    print ("Odd")
#if the user enters a number that is not divisible by 2, then "Odd" will be printed
